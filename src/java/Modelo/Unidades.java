package Modelo;

public class Unidades {

    private int idUnidades;
    private String descripcionUnidades;

    public int getIdUnidades() {
        return idUnidades;
    }

    public void setIdUnidades(int idUnidades) {
        this.idUnidades = idUnidades;
    }

    public String getDescripcionUnidades() {
        return descripcionUnidades;
    }

    public void setDescripcionUnidades(String descripcionUnidades) {
        this.descripcionUnidades = descripcionUnidades;
    }

}
